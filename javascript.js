    const stockClass = product.stock > 5 ? 'in-stock' : product.stock > 0 ? 'low-stock' : 'out-of-stock';
    const stockText = product.stock > 5 ? 'Tersedia' : product.stock > 0 ? 'Sisa ' + product.stock : 'Habis';
    let badge = '';
    if (product.badge === 'diskon' && discount > 0) badge = `<span class="product-badge badge-discount">${discount}% OFF</span>`;
    else if (product.badge === 'new') badge = `<span class="product-badge badge-new">New</span>`;
    else if (product.badge === 'best-seller') badge = `<span class="product-badge badge-best">⭐ Best Seller</span>`;
    if (product.stock === 0) badge = `<span class="product-badge badge-sold">Sold Out</span>`;
    const stars = '★'.repeat(Math.floor(product.rating || 0)) + ((product.rating || 0) % 1 >= 0.5 ? '★' : '');
