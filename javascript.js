function handleAuth(e) {
    e.preventDefault();
    const mode = document.getElementById('authForm').dataset.mode;
    const email = document.getElementById('authEmail').value.trim();
    const password = document.getElementById('authPassword').value;

    // Check admin login
    if (mode === 'login' && email === ADMIN_EMAIL && password === ADMIN_PASSWORD) {
        let users = getUsers();
        let adminUser = users.find(u => u.email === ADMIN_EMAIL);
        
        if (!adminUser) {
            adminUser = {
                id: 'admin_' + Date.now(),
                name: 'Administrator',
                email: ADMIN_EMAIL,
                password: ADMIN_PASSWORD,
                isAdmin: true,
                phone: '',
                address: '',
                photo: ''
            };
            users.push(adminUser);
            saveUsers(users);
        } else if (!adminUser.isAdmin) {
            adminUser.isAdmin = true;
            adminUser.password = ADMIN_PASSWORD;
            saveUsers(users);
        }
        
        saveCurrentUser(adminUser);
        document.getElementById('authModal').classList.remove('show');
        showToast(t('adminWelcome'), 'success');
        updateUserUI(adminUser);
        showAdminPanel();
        return false;
    }

    // Login customer
    if (mode === 'login') {
        const users = getUsers();
        const user = users.find(u => u.email === email && u.password === password);
        if (!user) {
            showToast(t('errorLogin'), 'error');
            return false;
        }
        if (user.isAdmin) {
            saveCurrentUser(user);
            document.getElementById('authModal').classList.remove('show');
            updateUserUI(user);
            showAdminPanel();
            return false;
        }
        saveCurrentUser(user);
        updateUserUI(user);
        document.getElementById('authModal').classList.remove('show');
        showToast(t('loginSuccess', { name: user.name }), 'success');
        updateWishlistBadge();
        translatePage();
        return false;
    }

    // Register
    if (mode === 'register') {
        const name = document.getElementById('regName').value.trim();
        const confirm = document.getElementById('confirmPassword').value;
        if (!name) { showToast(t('errorName'), 'error'); return false; }
        if (password !== confirm) { showToast(t('errorPassword'), 'error'); return false; }
        if (password.length < 6) { showToast(t('errorMinPassword'), 'error'); return false; }
        if (email === ADMIN_EMAIL) {
            showToast('Email ini tidak tersedia untuk registrasi!', 'error');
            return false;
        }
        const users = getUsers();
        if (users.find(u => u.email === email)) {
            showToast(t('errorRegister'), 'error');
            return false;
        }
        const newUser = { 
            id: Date.now(), 
            name, 
            email, 
            password, 
            isAdmin: false, 
            phone: '', 
            address: '', 
            photo: '' 
        };
        users.push(newUser);
        saveUsers(users);
        saveCurrentUser(newUser);
        updateUserUI(newUser);
        document.getElementById('authModal').classList.remove('show');
        showToast(t('registerSuccess', { name: name }), 'success');
        updateWishlistBadge();
        translatePage();
        return false;
    }

    // Forgot password
    if (mode === 'forgot') {
        const users = getUsers();
        const user = users.find(u => u.email === email);
        if (!user) {
            showToast('Email tidak ditemukan!', 'error');
            return false;
        }
        showToast('Link reset password telah dikirim ke ' + email, 'success');
        openAuthModal('login');
        return false;
    }
    return false;
}

