function handleProfilePhoto(e) {
    const file = e.target.files[0];
    if (!file) return;
    const reader = new FileReader();
    reader.onload = function(event) {
        const user = getCurrentUser();
        if (!user) return;
        user.photo = event.target.result;
        const users = getUsers();
        const idx = users.findIndex(u => u.id === user.id);
        if (idx !== -1) users[idx] = user;
        saveUsers(users);
        saveCurrentUser(user);
        loadProfile();
        updateUserUI(user);
        showToast(t('photoUpdate'), 'success');
    };
    reader.readAsDataURL(file);
}

function handleProfileUpdate(e) {
    e.preventDefault();
    const user = getCurrentUser();
    if (!user) { showToast(t('loginRequired'), 'error'); return false; }
    const name = document.getElementById('profileName').value.trim();
    const phone = document.getElementById('profilePhone').value.trim();
    const address = document.getElementById('profileAddress').value.trim();
    const newPassword = document.getElementById('profilePassword').value;

    const users = getUsers();
    const idx = users.findIndex(u => u.id === user.id);
    if (idx === -1) { showToast('User tidak ditemukan', 'error'); return false; }

    users[idx].name = name || users[idx].name;
    users[idx].phone = phone || users[idx].phone;
    users[idx].address = address || users[idx].address;
    if (newPassword && newPassword.length >= 6) {
        users[idx].password = newPassword;
    }
    saveUsers(users);
    saveCurrentUser(users[idx]);
    updateUserUI(users[idx]);
    showToast(t('profileUpdate'), 'success');
    return false;
}

