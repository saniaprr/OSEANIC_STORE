function submitReview() {
    const user = getCurrentUser();
    if (!user) { showToast(t('loginRequired'), 'error'); openAuthModal('login'); return; }
    if (!currentReviewProductId) { showToast('Produk tidak ditemukan', 'error'); return; }
    if (selectedRating === 0) { showToast('Pilih rating terlebih dahulu!', 'error'); return; }
    const text = document.getElementById('reviewText').value.trim();
    if (!text) { showToast('Tulis review kamu!', 'error'); return; }

    const fileInput = document.getElementById('reviewImage');
    const file = fileInput.files[0];

    const processReview = (imageData) => {
        const reviews = getReviews();
        reviews.push({
            id: Date.now(),
            productId: currentReviewProductId,
            userId: user.id,
            userName: user.name,
            rating: selectedRating,
            text: text,
            image: imageData || '',
            date: new Date().toISOString(),
            adminReply: ''
        });
        saveReviews(reviews);
        document.getElementById('reviewText').value = '';
        fileInput.value = '';
        setRating(0);
        loadReviews(currentReviewProductId);
        updateAdminStats();
        showToast(t('reviewSuccess'), 'success');
    };

    if (file) {
        const reader = new FileReader();
        reader.onload = function(e) {
            processReview(e.target.result);
        };
        reader.readAsDataURL(file);
    } else {
        processReview(null);
    }
}

