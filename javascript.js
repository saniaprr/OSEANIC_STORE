function updateQty(id, delta) {
    const cart = getCart();
    const item = cart.find(i => i.id === id);
    if (!item) return;
    const products = getProducts();
    const product = getProductById(id, products);
    if (delta > 0 && product && item.quantity >= product.stock) {
        showToast(t('errorStock'), 'error');
        return;
    }
    item.quantity += delta;
    if (item.quantity <= 0) {
        const idx = cart.indexOf(item);
        cart.splice(idx, 1);
    }
    saveCart(cart);
    renderCart();
    updateCartBadge();
}

function removeItem(id) {
    let cart = getCart();
    cart = cart.filter(i => i.id !== id);
    saveCart(cart);
    renderCart();
    updateCartBadge();
    showToast(t('itemRemoved'), 'info');
}

