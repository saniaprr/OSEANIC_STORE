function showPage(page) {
    try {
        const user = getCurrentUser();
        if (user && user.isAdmin) {
            showAdminPanel();
            return;
        }
        const pages = ['home', 'products', 'cart', 'checkout', 'wishlist', 'orders', 'profile', 'about', 'contact', 'faq'];
        pages.forEach(p => {
            const el = document.getElementById('page-' + p);
            if (el) el.style.display = p === page ? 'block' : 'none';
        });
        document.querySelectorAll('.nav-links a').forEach(a => {
            a.classList.toggle('active', a.dataset.page === page);
        });
        closeSearch();
        if (page === 'cart') renderCart();
        if (page === 'checkout') { renderCheckoutSummary(); updateShipping(); }
        if (page === 'products') { renderAllProducts(); applyFilters(); }
        if (page === 'wishlist') renderWishlist();
        if (page === 'orders') renderOrders();
        if (page === 'profile') loadProfile();
        window.scrollTo({ top: 0, behavior: 'smooth' });
        const navLinks = document.getElementById('navLinks');
        const navOverlay = document.getElementById('navOverlay');
        const hamburger = document.getElementById('hamburger');
        const userDropdown = document.getElementById('userDropdown');
        if (navLinks) navLinks.classList.remove('open');
        if (navOverlay) navOverlay.classList.remove('active');
        if (hamburger) hamburger.classList.remove('active');
        if (userDropdown) userDropdown.classList.remove('active');
        translatePage();
    } catch (error) {
        console.error('Show page error:', error);
    }
}

