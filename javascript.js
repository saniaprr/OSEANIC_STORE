function renderAdminAnalytics() {
    const orders = getOrders();
    const products = getProducts();
    const users = getUsers().filter(u => !u.isAdmin);
    
    const totalRevenue = orders.reduce((s, o) => {
        if (o.status !== 'cancelled') return s + (o.total || 0);
        return s;
    }, 0);
    
    const currentMonth = new Date().getMonth();
    const currentYear = new Date().getFullYear();
    const monthlyOrders = orders.filter(o => {
        const d = new Date(o.date);
        return d.getMonth() === currentMonth && d.getFullYear() === currentYear && o.status !== 'cancelled';
    });
    const monthlyRevenue = monthlyOrders.reduce((s, o) => s + (o.total || 0), 0);
    
    document.getElementById('analyticsRevenue').textContent = formatPrice(totalRevenue);
    document.getElementById('analyticsMonthlySales').textContent = monthlyOrders.length;
    document.getElementById('analyticsOrders').textContent = orders.length;
    
    const sales = {};
    orders.forEach(o => {
        if (o.status === 'cancelled') return;
        o.items.forEach(item => {
            sales[item.id] = (sales[item.id] || 0) + item.quantity;
        });
    });
    const sorted = Object.entries(sales).sort((a, b) => b[1] - a[1]);
    if (sorted.length > 0) {
        const best = getProductById(parseInt(sorted[0][0]), products);
        document.getElementById('analyticsBestSeller').textContent = best ? `${best.name} (${sorted[0][1]}x terjual)` : '-';
    } else {
        document.getElementById('analyticsBestSeller').textContent = '-';
    }
    
    renderSalesChart(orders);
    renderOrdersChart(orders);
    translatePage();
}

function renderSalesChart(orders) {
    const canvas = document.getElementById('salesChart');
    if (!canvas) return;
    const ctx = canvas.getContext('2d');
    if (adminChartInstances.sales) {
        adminChartInstances.sales.destroy();
    }
    
    const months = {};
    orders.forEach(o => {
        if (o.status === 'cancelled') return;
        const d = new Date(o.date);
        const key = d.getFullYear() + '-' + String(d.getMonth()+1).padStart(2,'0');
        months[key] = (months[key] || 0) + (o.total || 0);
    });
    
    const sortedMonths = Object.keys(months).sort();
    const labels = sortedMonths.map(m => {
        const [year, month] = m.split('-');
        const monthNames = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];
        return monthNames[parseInt(month)-1] + ' ' + year;
    });
    const data = sortedMonths.map(m => months[m]);
    
    adminChartInstances.sales = new Chart(ctx, {
        type: 'line',
        data: {
            labels: labels.length ? labels : ['No Data'],
            datasets: [{
                label: 'Revenue',
                data: data.length ? data : [0],
                borderColor: '#c49a6c',
                backgroundColor: 'rgba(196,154,108,0.1)',
                fill: true,
                tension: 0.4
            }]
        },
        options: {
            responsive: true,
            maintainAspectRatio: false,
            plugins: { legend: { display: false } }
        }
    });
}

function renderOrdersChart(orders) {
    const canvas = document.getElementById('ordersChart');
    if (!canvas) return;
    const ctx = canvas.getContext('2d');
    if (adminChartInstances.orders) {
        adminChartInstances.orders.destroy();
    }
    
    const statusCounts = {
        pending: 0,
        paid: 0,
        processing: 0,
        packed: 0,
        shipped: 0,
        delivered: 0,
        completed: 0,
        cancelled: 0
    };
    orders.forEach(o => {
        if (statusCounts[o.status] !== undefined) {
            statusCounts[o.status]++;
        }
    });
    
    adminChartInstances.orders = new Chart(ctx, {
        type: 'bar',
        data: {
            labels: Object.keys(statusCounts).map(s => s.charAt(0).toUpperCase() + s.slice(1)),
            datasets: [{
                label: 'Orders by Status',
                data: Object.values(statusCounts),
                backgroundColor: ['#ffc107','#ffc107','#17a2b8','#6c757d','#007bff','#28a745','#28a745','#dc3545']
            }]
        },
        options: {
            responsive: true,
            maintainAspectRatio: false,
            plugins: { legend: { display: false } }
        }
    });
}

