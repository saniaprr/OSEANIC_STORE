function saveProduct(e) {
    e.preventDefault();
    const id = document.getElementById('productFormId').value;
    const name = document.getElementById('pName').value.trim();
    const category = document.getElementById('pCategory').value;
    const price = parseInt(document.getElementById('pPrice').value);
    const originalPrice = parseInt(document.getElementById('pDiscountPrice').value) || 0;
    const description = document.getElementById('pDescription').value.trim();
    const brand = document.getElementById('pBrand').value.trim();
    const color = document.getElementById('pColor').value.trim();
    const material = document.getElementById('pMaterial').value.trim();
    const condition = document.getElementById('pCondition').value;
    const sizes = document.getElementById('pSizes').value.split(',').map(s => s.trim()).filter(s => s);
    const weight = parseInt(document.getElementById('pWeight').value) || 0;
    const stock = parseInt(document.getElementById('pStock').value);
    const image = document.getElementById('pImage').value.trim() || 'https://via.placeholder.com/300x300?text=No+Image';
    const featured = document.getElementById('pFeatured').checked;

    if (!name || !price || !description) {
        showToast('Mohon lengkapi data produk!', 'error');
        return false;
    }

    let products = getProducts();
    const productData = {
        name,
        category,
        price,
        originalPrice: originalPrice > price ? originalPrice : price,
        description,
        brand,
        color,
        material,
        condition,
        size: sizes.length ? sizes : ['One Size'],
        weight,
        stock,
        image,
        featured,
        rating: 4.5,
        reviews: 0,
        badge: originalPrice > price ? 'diskon' : 'new'
    };

    if (id) {
        const idx = products.findIndex(p => p.id === parseInt(id));
        if (idx !== -1) {
            products[idx] = { ...products[idx], ...productData };
            saveProducts(products);
            showToast(t('productUpdated'), 'success');
        }
    } else {
        const newProduct = {
            id: getNextId(products),
            ...productData
        };
        products.push(newProduct);
        saveProducts(products);
        showToast(t('productAdded'), 'success');
    }

    closeProductModal();
    renderAdminProducts();
    updateAdminStats();
    renderAllProducts();
    renderFeaturedProducts();
    renderCategories();
    return false;
}

