function renderBestSelling(orders, products) {
    const container = document.getElementById('bestSellingProducts');
    if (!container) return;
    if (orders.length === 0 || products.length === 0) {
        container.innerHTML = '<p style="color:var(--gray);text-align:center;padding:1rem 0;">Belum ada data penjualan</p>';
        return;
    }
    const sales = {};
    orders.forEach(o => {
        if (o.status === 'cancelled') return;
        o.items.forEach(item => {
            sales[item.id] = (sales[item.id] || 0) + item.quantity;
        });
    });
    const sorted = Object.entries(sales).sort((a, b) => b[1] - a[1]).slice(0, 5);
    if (sorted.length === 0) {
        container.innerHTML = '<p style="color:var(--gray);text-align:center;padding:1rem 0;">Belum ada penjualan</p>';
        return;
    }
    container.innerHTML = sorted.map(([id, qty]) => {
        const product = getProductById(parseInt(id), products);
        return product ? `
            <div style="display:flex;justify-content:space-between;padding:0.5rem 0;border-bottom:1px solid var(--border-color);font-size:0.85rem;">
                <span>${product.name}</span>
                <span style="color:var(--accent);">${qty}x terjual</span>
            </div>
        ` : '';
    }).join('');
}

