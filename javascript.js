function updateUserUI(user) {
    const avatar = document.getElementById('userAvatar');
    const info = document.getElementById('userInfo');
    if (user) {
        const initial = user.name ? user.name.charAt(0).toUpperCase() : 'U';
        if (avatar) avatar.innerHTML = initial;
        if (info) info.innerHTML = '👤 ' + (user.isAdmin ? 'Admin' : user.name);
        // Show admin link in dropdown if admin
        const dropdown = document.getElementById('userDropdown');
        if (dropdown && user.isAdmin) {
            // Check if admin link already exists
            let adminLink = dropdown.querySelector('.admin-link');
            if (!adminLink) {
                const logoutBtn = dropdown.querySelector('.logout-btn');
                const link = document.createElement('a');
                link.className = 'admin-link';
                link.innerHTML = '<i class="fas fa-crown"></i> <span data-key="adminDashboard">Dashboard</span>';
                link.onclick = function(e) {
                    e.preventDefault();
                    showAdminPanel();
                };
                if (logoutBtn) {
                    dropdown.insertBefore(link, logoutBtn);
                } else {
                    dropdown.appendChild(link);
                }
            }
        }
    } else {
        if (avatar) avatar.innerHTML = '<i class="fas fa-user"></i>';
        if (info) info.innerHTML = 'Guest';
        // Remove admin link if exists
        const dropdown = document.getElementById('userDropdown');
        if (dropdown) {
            const adminLink = dropdown.querySelector('.admin-link');
            if (adminLink) adminLink.remove();
        }
    }
}

