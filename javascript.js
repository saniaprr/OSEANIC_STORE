function updateOrderStatus(orderId, newStatus) {
    const orders = getOrders();
    const order = orders.find(o => o.id === orderId);
    if (!order) return;
    
    const statusIndex = {
        pending: 0,
        paid: 0,
        processing: 1,
        packed: 2,
        shipped: 3,
        delivered: 4,
        completed: 4,
        cancelled: -1
    };
    
    order.status = newStatus;
    if (statusIndex[newStatus] !== undefined && statusIndex[newStatus] >= 0) {
        order.currentStep = statusIndex[newStatus];
    }
    saveOrders(orders);
    renderAdminOrders();
    updateAdminStats();
    showToast(t('orderUpdated'), 'success');
}

